function Com1()
    set(VHF1Roll_1A, math.floor(get(Com1LeftFreq)/10000))
    set(VHF1Roll_1B, get(VHF1Roll_1B)+((math.floor((get(Com1LeftFreq)/1000)) - get(VHF1Roll_1B))*RR*30))
    set(VHF1Roll_1C, get(VHF1Roll_1C)+((math.floor((get(Com1LeftFreq)/100)) - get(VHF1Roll_1C))*RR*30))
    set(VHF1Roll_1D, math.floor(get(Com1LeftFreq)/10))
    set(VHF1Roll_1E, math.floor(get(Com1LeftFreq)/1))
    
    set(VHF1Roll_2A, math.floor(get(Com1RightFreq)/10000))
    set(VHF1Roll_2B, get(VHF1Roll_2B)+((math.floor((get(Com1RightFreq)/1000)) - get(VHF1Roll_2B))*RR*30))
    set(VHF1Roll_2C, get(VHF1Roll_2C)+((math.floor((get(Com1RightFreq)/100)) - get(VHF1Roll_2C))*RR*30))
    set(VHF1Roll_2D, math.floor(get(Com1RightFreq)/10))
    set(VHF1Roll_2E, math.floor(get(Com1RightFreq)/1))
    
    if get(Com1FreqIsRight) == 0 then
        set(Com1Light1, get(Com1Light1)+((1*get(pow) - get(Com1Light1))*RR*20))
        set(Com1Light2, get(Com1Light2)+((0*get(pow) - get(Com1Light2))*RR*20))
    else
        set(Com1Light1, get(Com1Light1)+((0*get(pow) - get(Com1Light1))*RR*20))
        set(Com1Light2, get(Com1Light2)+((1*get(pow) - get(Com1Light2))*RR*20))
    end
end

